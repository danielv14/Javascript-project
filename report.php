                                <h2>Krav k1: Paketera, presentera och produktifiera</h2>

<p>För mitt slutprojekt i denna kurs har jag skapat en hemsida där man kan söka efter Instagram hashtags och se ett galleri enligt den sökta hashtagen samt en slideshow med en förbestämd hashtag. Jag har namngivit projektet och hemsidan Insta-site.
Hemsidan är byggd med hjälp av bootstrap men använder dock ett annat tema än bootstraps originaltema.
I övrigt är hemsidan väldigt enkel jag försökte att lyfta fram galleriet och min slideshow direkt på startsidan och jag försökte även snabbt få potentiella användare att inse vad hemsidan handlar om. I Footern finns en länk som tar en till GitHub där en avskalad version av Insta-site finns som enbart lyfter fram själva galleriet och bildspelet. På Github finns enkla instruktioner hur man kan konfigurera applikationerna, dessa instruktioner finns även på ”about”-sidan på Insta-site.
Applikationerna är byggda på javascript och jquery. Galleriet bygger även på AngularJS för att få till sökfunktionen i galleriet.</p>

<p>Jag skulle själv påstå att jag lyckats produktifiera projektet bra då jag visar upp och ”marknadsför" galleriet och bildspelet på Insta-site på ett någorlunda professionellt  sätt. Applikationerna är även paketerade på ett avskalat sätt på GitHub. Hade först tänk länka till ett repository som visar källkoden för hela Insta-site men insåg efter ett tag att det vore smidigare att kunna visa upp ett mer avskalat repository som lyfter fram applikationerna utan allt ”runtikring” som presentationssidan för Insta-site annars är uppbyggt av såsom exempelvis bootstrap och uppdelning av kod.</p>

<h2>Krav k2: Ha koll på konkurrenterna och lär av dem</h2>

<p>Om man googlar lite snabbt efter nyckelord som ’instagram feeds’ och liknande hittar man andra webbapplikationer. Många av konkurrenterna erbjuder en tjänst som är mer omfattande än den jag har skapat i mitt slutprojekt. Det jag snabbt insåg är att om man vill få ut så mycket som möjligt ur en Instagram webbapplikation måste man logga in via ett Instagramkonto. Detta skapar då bland annat möjligheten att kommentera och gilla bilder.</p>

<p>Till exempel så är både <a href="http://pinsta.me/" title="Pinsta.me">Pinsta</a> och <a href="http://iconosquare.com" title="Iconosquare.com">IconoSquare</a> mer funktionsrika än Insta-Site men båda kräver login för att kunna använda applikationerna till fullo. Å andra sidan har <a href="vsmaster.com" title="VSmaster.com">VSMaster.com</a> skapat två olika webbapplikationer knutna till Instagram.  <a href="logingram.com" title="LoginGram.com">LoginGram</a> kräver inloggning för att ens komma åt applikationen medan <a href="findgram.com" title="Findgram.com">FindGram</a> som de också erbjuder inte har inlogning som krav.     <br/>
Funktionaliteten beror alltså mycket på hur man använder sig av Instagrams API och om inloggningsmöjligheter erbjuds eller ej. För många personer kanske det räcker med en webbapplikation likt FindGram som är enklare att använda medan andra personer verkligen vill kunna använda webbapplikationen precis som om det vore Instagrams eget program med all dess funktionalitet.</p>

<p>Som en version 2.0 av Insta-site skulle man kunna implementera inloggningsmöjligheter för att låt användare kommentera och gilla bilder som man sökt efter i galleriet. Möjligheten att söka efter Instagram-användare och inte bara hashtags skulle också göra Insta-site mer rik på funktioner. Detta kräver dock inte att inloggningsuppgifter.
Frågan är hur pass mer avancerad man kan göra Insta-site och samtidigt behålla de två potentiella målgrupperna där den ena strävar efter enkelhet medan den andra vill ha ”full” funktionalitet likt Instagrams egna applikationer.</p>

